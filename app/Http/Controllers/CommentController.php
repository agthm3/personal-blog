     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'content' => 'required'
        ]);

        $user_id = Auth::id();

        Comment::create([
            'content' => $request->content,
            'article_id' => $request->article_id,
            'user_id' => $user_id
        ]);

        return Redirect::back();
